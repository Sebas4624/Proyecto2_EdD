
package proyecto2_edd;



public class ClienteHospedado {
    
    public String ci;
    public String habitacion;
    public String nombre;
    public String apellido;
    public String email;
    public String genero;
    public String celular;
    public String llegada;
    public String salida;
    public String tipoHabitacion;
    
    public ClienteHospedado(String ci, String habitacion, String nombre, String apellido, String email, String genero, String celular, String llegada, String salida, String tipoHabitacion) {
        this.ci = ci;
        this.habitacion = habitacion;
        this.nombre = nombre;
        this.apellido = apellido;
        this.email = email;
        this.genero = genero;
        this.celular = celular;
        this.llegada = llegada;
        this.salida = salida;
        this.tipoHabitacion = tipoHabitacion;
    }
    
}
